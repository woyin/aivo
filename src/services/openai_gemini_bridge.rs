/// Documented Gemini placeholder for the `thoughtSignature` field on
/// functionCall parts whose original signature is unavailable (e.g. histories
/// not generated by Gemini, or any case where aivo's per-process cache misses).
/// Source: https://ai.google.dev/gemini-api/docs/thought-signatures
pub(crate) const SKIP_THOUGHT_SIGNATURE_PLACEHOLDER: &str = "skip_thought_signature_validator";

/// How long a captured signature stays usable. Gemini's strict validation
/// only covers the *current* assistant turn, so signatures older than this
/// have very low odds of being needed and are pruned to bound disk growth.
const SIGNATURE_TTL_SECS: u64 = 24 * 60 * 60;

#[derive(Clone, Serialize, Deserialize)]
struct SignatureEntry {
    signature: String,
    captured_at: u64,
}

fn cache_key(model: &str, call_id: &str) -> String {
    // Keying by (model, call_id) prevents cross-model contamination — a stale
    // signature captured against `gemini-2.5-pro` would otherwise be replayed
    // against `gemini-3.1-pro-preview` and trigger a 400.
    format!("{}|{}", model, call_id)
}

/// Process-wide cache of `(model, call_id) -> thoughtSignature`. Gemini-3
/// returns a `thoughtSignature` on functionCall response parts and rejects
/// subsequent turns whose history doesn't echo it back. Claude Code (the
/// upstream) drops unknown fields on tool blocks, so we maintain the mapping
/// here and re-attach signatures when the request flows back through.
///
/// Persisted to disk so that a process restart between turns
/// (e.g., re-launching `aivo run gemini`) doesn't lose the signatures.
fn signature_cache() -> &'static Mutex<HashMap<String, SignatureEntry>> {
    static CACHE: OnceLock<Mutex<HashMap<String, SignatureEntry>>> = OnceLock::new();
    CACHE.get_or_init(|| {
        let mut map = HashMap::new();
        if let Some(disk) = read_signature_store_from_disk() {
            map.extend(disk);
        }
        prune_expired(&mut map, current_unix_ts());
        Mutex::new(map)
    })
}

fn signature_store_path() -> Option<PathBuf> {
    Some(crate::services::paths::gemini_thought_signatures(
        &crate::services::paths::config_dir(),
    ))
}

fn read_signature_store_from_disk() -> Option<HashMap<String, SignatureEntry>> {
    let path = signature_store_path()?;
    let contents = std::fs::read_to_string(&path).ok()?;
    serde_json::from_str::<HashMap<String, SignatureEntry>>(&contents).ok()
}

fn prune_expired(map: &mut HashMap<String, SignatureEntry>, now: u64) {
    map.retain(|_, entry| now.saturating_sub(entry.captured_at) < SIGNATURE_TTL_SECS);
}

/// Best-effort write of the cache to disk. Reads the current disk state and
/// merges the in-memory cache on top before writing back, so concurrent
/// `aivo` processes preserve each other's recently-captured entries instead
/// of last-writer-wins clobbering them. Errors are swallowed: a missing
/// signature degrades to `SKIP_THOUGHT_SIGNATURE_PLACEHOLDER` rather than
/// failing the request outright.
fn persist_signature_store(map: &HashMap<String, SignatureEntry>) {
    let Some(path) = signature_store_path() else {
        return;
    };
    if let Some(parent) = path.parent() {
        let _ = std::fs::create_dir_all(parent);
    }
    let mut merged = read_signature_store_from_disk().unwrap_or_default();
    // Newer in-memory entries override older on-disk entries for the same key.
    for (key, entry) in map {
        let keep_existing = merged
            .get(key)
            .map(|existing| existing.captured_at > entry.captured_at)
            .unwrap_or(false);
        if !keep_existing {
            merged.insert(key.clone(), entry.clone());
        }
    }
    prune_expired(&mut merged, current_unix_ts());
    let Ok(json) = serde_json::to_string(&merged) else {
        return;
    };
    write_string_atomic(&path, &json);
}

/// Synchronous atomic write: temp file under the same dir + rename + 0o600
/// on Unix. Mirrors `atomic_write::atomic_write_secure` which is async, but
/// uses a per-writer timestamped temp name so concurrent writers don't
/// truncate each other's in-flight data.
fn write_string_atomic(path: &PathBuf, contents: &str) {
    use std::io::Write;
    let Some(parent) = path.parent() else {
        return;
    };
    let _ = std::fs::create_dir_all(parent);
    let temp_path = parent.join(format!(
        ".gemini_thought_signatures.{}.tmp",
        current_unix_ts()
    ));

    let mut opts = std::fs::OpenOptions::new();
    opts.write(true).create(true).truncate(true);
    #[cfg(unix)]
    {
        use std::os::unix::fs::OpenOptionsExt;
        // Restrict reads from the start so there's no world-readable window.
        opts.mode(0o600);
    }
    #[cfg(windows)]
    {
        use std::os::windows::fs::OpenOptionsExt;
        // No-share while open; ACL inheritance from the parent dir handles
        // permissions on Windows.
        opts.share_mode(0);
    }
    let Ok(mut file) = opts.open(&temp_path) else {
        return;
    };
    if file.write_all(contents.as_bytes()).is_err() {
        let _ = std::fs::remove_file(&temp_path);
        return;
    }
    let _ = file.sync_all();
    drop(file);
    let _ = std::fs::rename(&temp_path, path);
}

fn remember_signature(model: &str, call_id: &str, signature: &str) {
    if model.is_empty() || call_id.is_empty() || signature.is_empty() {
        return;
    }
    let key = cache_key(model, call_id);
    let entry = SignatureEntry {
        signature: signature.to_string(),
        captured_at: current_unix_ts(),
    };
    let snapshot = if let Ok(mut cache) = signature_cache().lock() {
        cache.insert(key, entry);
        Some(cache.clone())
    } else {
        None
    };
    if let Some(snapshot) = snapshot {
        persist_signature_store(&snapshot);
    }
}

fn lookup_signature(model: &str, call_id: &str) -> Option<String> {
    if model.is_empty() || call_id.is_empty() {
        return None;
    }
    let key = cache_key(model, call_id);
    let now = current_unix_ts();
    let cache = signature_cache().lock().ok()?;
    cache
        .get(&key)
        .filter(|entry| now.saturating_sub(entry.captured_at) < SIGNATURE_TTL_SECS)
        .map(|entry| entry.signature.clone())
}

/// Gemini's `functionDeclarations` only accepts a subset of JSON Schema fields.
/// Recursively strip unsupported fields like `$schema`, `additionalProperties`,
/// `propertyNames`, etc. that OpenAI-compatible tool definitions commonly include.
pub fn sanitize_schema_for_gemini(schema: &Value) -> Value {
    let Some(obj) = schema.as_object() else {
        return schema.clone();
    };

    // Gemini rejects schemas where `anyOf` appears alongside sibling
    // schema-defining fields (`type`, `properties`, `required`, etc.) with
    // 400 INVALID_ARGUMENT: "When using any_of, it must be the only field set."
    // Emit only `anyOf` (recursively sanitized) at this level when present.
    if let Some(any_of) = obj.get("anyOf").and_then(|v| v.as_array()) {
        let cleaned_variants: Vec<Value> = any_of.iter().map(sanitize_schema_for_gemini).collect();
        let mut out = serde_json::Map::new();
        out.insert("anyOf".to_string(), Value::Array(cleaned_variants));
        return Value::Object(out);
    }

    static ALLOWED: &[&str] = &[
        "type",
        "description",
        "properties",
        "required",
        "items",
        "enum",
        "format",
        "nullable",
        "minimum",
        "maximum",
        "minItems",
        "maxItems",
    ];

    let mut cleaned = serde_json::Map::new();
    for key in ALLOWED {
        if let Some(value) = obj.get(*key) {
            let sanitized = match *key {
                "properties" => {
                    if let Some(props) = value.as_object() {
                        let mut new_props = serde_json::Map::new();
                        for (k, v) in props {
                            new_props.insert(k.clone(), sanitize_schema_for_gemini(v));
                        }
                        Value::Object(new_props)
                    } else {
                        value.clone()
                    }
                }
                "items" => sanitize_schema_for_gemini(value),
                _ => value.clone(),
            };
            cleaned.insert(key.to_string(), sanitized);
        }
    }

    // Gemini's schema `type` must be a single string; JSON Schema also allows an
    // array (e.g. `["string","null"]` for nullable/union). Gemini rejects the array
    // ("Proto field is not repeating"), so collapse to the first non-null member and
    // mark the field nullable when "null" was present.
    if let Some(types) = cleaned.get("type").and_then(|v| v.as_array()).cloned() {
        let has_null = types.iter().any(|t| t.as_str() == Some("null"));
        let primary = types
            .iter()
            .find(|t| t.as_str().is_some_and(|s| s != "null"))
            .cloned()
            .unwrap_or_else(|| json!("object"));
        cleaned.insert("type".to_string(), primary);
        if has_null {
            cleaned.insert("nullable".to_string(), json!(true));
        }
    }

    // Ensure type and properties exist for object schemas
    // Treat null type the same as missing
    if !cleaned.contains_key("type") || cleaned.get("type").is_some_and(|v| v.is_null()) {
        cleaned.insert("type".to_string(), json!("object"));
    }
    if cleaned.get("type").and_then(|v| v.as_str()) == Some("object")
        && !cleaned.contains_key("properties")
    {
        cleaned.insert("properties".to_string(), json!({}));
    }

    Value::Object(cleaned)
}

#[derive(Clone, Copy, Debug)]
pub struct OpenAIToGeminiConfig<'a> {
    pub default_model: &'a str,
}

pub fn openai_chat_model(body: &Value, default_model: &str) -> String {
    body.get("model")
        .and_then(|m| m.as_str())
        .unwrap_or(default_model)
        .to_string()
}

pub fn build_google_generate_content_url(base_url: &str, model: &str) -> String {
    build_google_content_url(base_url, model, false)
}

pub fn build_google_stream_generate_content_url(base_url: &str, model: &str) -> String {
    build_google_content_url(base_url, model, true)
}

fn build_google_content_url(base_url: &str, model: &str, stream: bool) -> String {
    let base = base_url.trim_end_matches('/');
    let model = google_native_model_name(model);
    let suffix = if stream {
        ":streamGenerateContent?alt=sse"
    } else {
        ":generateContent"
    };
    if base.ends_with("/models") {
        format!("{}/{}{}", base, model, suffix)
    } else if base.ends_with("/v1beta") || base.ends_with("/v1") {
        format!("{}/models/{}{}", base, model, suffix)
    } else {
        format!("{}/v1beta/models/{}{}", base, model, suffix)
    }
}

pub fn convert_openai_chat_to_gemini_request(body: &Value, config: &OpenAIToGeminiConfig) -> Value {
    let mut system_parts: Vec<String> = Vec::new();
    let mut contents: Vec<Value> = Vec::new();
    let mut tool_names_by_call_id: HashMap<String, String> = HashMap::new();
    let request_model = openai_chat_model(body, config.default_model);

    if let Some(messages) = body.get("messages").and_then(|m| m.as_array()) {
        let mut iter = messages.iter().peekable();
        while let Some(message) = iter.next() {
            let role = message
                .get("role")
                .and_then(|r| r.as_str())
                .unwrap_or("user");

            match role {
                "system" => {
                    let text = extract_openai_text(message.get("content"));
                    if !text.is_empty() {
                        system_parts.push(text);
                    }
                }
                "assistant" => {
                    let mut parts: Vec<Value> = Vec::new();
                    let text = extract_openai_text(message.get("content"));
                    if !text.is_empty() {
                        parts.push(json!({ "text": text }));
                    }
                    if let Some(tool_calls) = message.get("tool_calls").and_then(|v| v.as_array()) {
                        for (tc_index, tool_call) in tool_calls.iter().enumerate() {
                            let name = tool_call
                                .get("function")
                                .and_then(|f| f.get("name"))
                                .and_then(|v| v.as_str())
                                .unwrap_or("");
                            let call_id = tool_call
                                .get("id")
                                .and_then(|v| v.as_str())
                                .filter(|id| !id.is_empty())
                                .map(ToOwned::to_owned)
                                .unwrap_or_else(|| format!("call_{tc_index}"));
                            tool_names_by_call_id.insert(call_id.clone(), name.to_string());
                            let args = tool_call
                                .get("function")
                                .and_then(|f| f.get("arguments"))
                                .and_then(|v| v.as_str())
                                .and_then(|s| serde_json::from_str::<Value>(s).ok())
                                .unwrap_or_else(|| json!({}));
                            let mut part = json!({
                                "functionCall": {
                                    "id": call_id,
                                    "name": name,
                                    "args": args
                                }
                            });
                            // Gemini-3's signature is per-turn, attached to the
                            // first part only. Real signature from this
                            // process's history if cached; otherwise the
                            // documented Google placeholder so we don't 400 on
                            // missing thought_signature. See:
                            // https://ai.google.dev/gemini-api/docs/thought-signatures
                            if tc_index == 0 {
                                let sig = lookup_signature(&request_model, &call_id)
                                    .unwrap_or_else(|| {
                                        SKIP_THOUGHT_SIGNATURE_PLACEHOLDER.to_string()
                                    });
                                part["thoughtSignature"] = Value::String(sig);
                            }
                            parts.push(part);
                        }
                    }
                    if !parts.is_empty() {
                        contents.push(json!({ "role": "model", "parts": parts }));
                    }
                }
                "tool" => {
                    // Gemini validates parallel tool responses as one logical
                    // user step. Group consecutive OpenAI `role: tool` messages
                    // so assistant[FC1,FC2], tool(FR1), tool(FR2) becomes
                    // model[FC1,FC2], user[FR1,FR2].
                    let mut parts = vec![build_tool_response_part(message, &tool_names_by_call_id)];
                    while iter
                        .peek()
                        .and_then(|m| m.get("role"))
                        .and_then(|r| r.as_str())
                        == Some("tool")
                    {
                        // peek+next must succeed by Iterator contract, but
                        // a defensive break keeps the loop robust to
                        // future refactors that touch the iterator type.
                        let Some(next) = iter.next() else { break };
                        parts.push(build_tool_response_part(next, &tool_names_by_call_id));
                    }
                    contents.push(json!({ "role": "user", "parts": parts }));
                }
                _ => {
                    let parts = extract_openai_parts_for_gemini(message.get("content"));
                    contents.push(json!({
                        "role": "user",
                        "parts": parts,
                    }));
                }
            }
        }
    }

    let mut request = json!({
        "contents": contents,
    });

    if !system_parts.is_empty() {
        request["systemInstruction"] = json!({
            "parts": [{
                "text": system_parts.join("\n\n")
            }]
        });
    }

    if let Some(tools) = body.get("tools").and_then(|t| t.as_array()) {
        let declarations: Vec<Value> = tools
            .iter()
            .filter(|tool| tool.get("type").and_then(|v| v.as_str()) == Some("function"))
            .map(|tool| {
                json!({
                    "name": tool.get("function").and_then(|f| f.get("name")).cloned().unwrap_or_default(),
                    "description": tool.get("function").and_then(|f| f.get("description")).cloned().unwrap_or(json!("")),
                    "parameters": sanitize_schema_for_gemini(
                        tool.get("function").and_then(|f| f.get("parameters")).unwrap_or(&json!({"type": "object", "properties": {}}))
                    )
                })
            })
            .collect();
        // Translate well-known OpenAI / Responses-API server tools to their
        // Gemini equivalents instead of dropping. Unknown server tools are
        // silently skipped (no Gemini equivalent).
        let mut gemini_tool_groups: Vec<Value> = Vec::new();
        if !declarations.is_empty() {
            gemini_tool_groups.push(json!({ "functionDeclarations": declarations }));
        }
        for tool in tools {
            match tool.get("type").and_then(|v| v.as_str()) {
                Some("web_search") | Some("web_search_preview") => {
                    gemini_tool_groups.push(json!({ "googleSearch": {} }));
                }
                Some("code_interpreter") => {
                    gemini_tool_groups.push(json!({ "codeExecution": {} }));
                }
                _ => {}
            }
        }
        if !gemini_tool_groups.is_empty() {
            request["tools"] = Value::Array(gemini_tool_groups);
        }
    }

    if let Some(choice) = body.get("tool_choice") {
        let function_calling_config = match choice {
            Value::String(value) if value == "auto" => Some(json!({ "mode": "AUTO" })),
            Value::String(value) if value == "none" => Some(json!({ "mode": "NONE" })),
            Value::String(value) if value == "required" => Some(json!({ "mode": "ANY" })),
            Value::Object(obj) if obj.get("type").and_then(|v| v.as_str()) == Some("function") => {
                obj.get("function")
                    .and_then(|f| f.get("name"))
                    .and_then(|v| v.as_str())
                    .map(|name| {
                        json!({
                            "mode": "ANY",
                            "allowedFunctionNames": [name]
                        })
                    })
            }
            _ => None,
        };
        if let Some(config_value) = function_calling_config {
            request["toolConfig"] = json!({ "functionCallingConfig": config_value });
        }
    }

    let mut generation = serde_json::Map::new();
    if let Some(v) = body.get("max_tokens") {
        generation.insert("maxOutputTokens".to_string(), v.clone());
    }
    // Dropped for models that reject sampling params (o-series etc.).
    if !crate::services::model_metadata::rejects_temperature(&request_model) {
        if let Some(v) = body.get("temperature") {
            generation.insert("temperature".to_string(), v.clone());
        }
        if let Some(v) = body.get("top_p") {
            generation.insert("topP".to_string(), v.clone());
        }
        if let Some(v) = body.get("top_k") {
            generation.insert("topK".to_string(), v.clone());
        }
    }
    if let Some(v) = body.get("seed") {
        generation.insert("seed".to_string(), v.clone());
    }
    // OpenAI `response_format: {type: "json_object"}` → Gemini
    // `responseMimeType: application/json`. Other response_format shapes
    // (json_schema with strict schema) are not converted yet.
    if let Some(rf) = body.get("response_format")
        && rf.get("type").and_then(|v| v.as_str()) == Some("json_object")
    {
        generation.insert(
            "responseMimeType".to_string(),
            Value::String("application/json".to_string()),
        );
    }
    if let Some(v) = body.get("stop") {
        generation.insert(
            "stopSequences".to_string(),
            crate::services::bridge_defaults::stop_sequences_array(v),
        );
    }
    // OpenAI `reasoning_effort` → Gemini `thinkingConfig`; the surface differs
    // by version (see `gemini_uses_thinking_level`).
    if !generation.contains_key("thinkingConfig")
        && let Some(effort) = extract_openai_effort(body)
        && let Some(cfg) =
            gemini_thinking_config(effort, gemini_uses_thinking_level(&request_model))
    {
        generation.insert("thinkingConfig".to_string(), cfg);
    }
    if !generation.is_empty() {
        request["generationConfig"] = Value::Object(generation);
    }

    if request["contents"].as_array().is_none_or(|c| c.is_empty()) {
        request["contents"] = json!([{
            "role": "user",
            "parts": [{ "text": "" }]
        }]);
    }

    let _ = config.default_model;
    request
}

pub fn convert_gemini_to_openai_chat_response(resp: &Value, fallback_model: &str) -> Value {
    let candidate = resp
        .get("candidates")
        .and_then(|v| v.as_array())
        .and_then(|arr| arr.first())
        .cloned()
        .unwrap_or_else(|| json!({}));
    let parts = candidate
        .get("content")
        .and_then(|content| content.get("parts"))
        .and_then(|v| v.as_array())
        .cloned()
        .unwrap_or_default();

    let mut text_parts: Vec<String> = Vec::new();
    let mut thought_parts: Vec<String> = Vec::new();
    let mut tool_calls: Vec<Value> = Vec::new();
    // The first part-position-based signature stands in for any non-functionCall
    // part (text, inlineData) that needs to be replayed in image-edit / thinking
    // flows. Per spec, signatures attach to the first emitted part of each
    // logical step, so we anchor it to the first tool call we see by
    // synthesizing a stable "turn" call_id.
    let mut leading_part_signature: Option<String> = None;
    for (index, part) in parts.iter().enumerate() {
        if let Some(text) = part.get("text").and_then(|v| v.as_str())
            && !text.is_empty()
        {
            // `thought: true` marks the thinking trace — surface it as
            // reasoning_content, never as user-visible text.
            if part
                .get("thought")
                .and_then(|t| t.as_bool())
                .unwrap_or(false)
            {
                thought_parts.push(text.to_string());
            } else {
                text_parts.push(text.to_string());
            }
        }
        if leading_part_signature.is_none()
            && let Some(sig) = part.get("thoughtSignature").and_then(|v| v.as_str())
            && !sig.is_empty()
        {
            leading_part_signature = Some(sig.to_string());
        }
        if let Some(function_call) = part.get("functionCall") {
            let args = function_call
                .get("args")
                .cloned()
                .unwrap_or_else(|| json!({}));
            let id = function_call
                .get("id")
                .and_then(|v| v.as_str())
                .filter(|id| !id.is_empty())
                .map(ToOwned::to_owned)
                .unwrap_or_else(|| format!("call_{index}"));
            if let Some(sig) = part.get("thoughtSignature").and_then(|v| v.as_str()) {
                remember_signature(fallback_model, &id, sig);
            } else if let Some(sig) = leading_part_signature.as_deref() {
                // Tool call without its own signature but a sibling text/inlineData
                // part carried one for this step — associate it with this call_id
                // so a subsequent turn replaying the same call_id still has it.
                remember_signature(fallback_model, &id, sig);
            }
            tool_calls.push(json!({
                "id": id,
                "type": "function",
                "function": {
                    "name": function_call.get("name").cloned().unwrap_or(json!("")),
                    "arguments": serde_json::to_string(&args).unwrap_or_else(|_| "{}".to_string())
                }
            }));
        }
    }

    let raw_finish_reason = candidate
        .get("finishReason")
        .and_then(|v| v.as_str())
        .unwrap_or("STOP");
    let finish_reason = match raw_finish_reason {
        "MAX_TOKENS" => "length",
        // RECITATION = Gemini detected the output verbatim-quotes training
        // data and blocked the response. Closer to OpenAI's "content_filter"
        // than to a normal stop.
        "SAFETY" | "RECITATION" => "content_filter",
        _ if !tool_calls.is_empty() => "tool_calls",
        _ => "stop",
    };

    let mut message = json!({
        "role": "assistant",
        "content": if text_parts.is_empty() { Value::Null } else { Value::String(text_parts.join("\n")) }
    });
    if message["content"].is_null()
        && let Some(obj) = message.as_object_mut()
    {
        obj.remove("content");
    }
    if !thought_parts.is_empty() {
        message["reasoning_content"] = Value::String(thought_parts.join("\n"));
    }
    if !tool_calls.is_empty() {
        message["tool_calls"] = Value::Array(tool_calls);
    }

    let prompt_tokens = resp
        .get("usageMetadata")
        .and_then(|u| u.get("promptTokenCount"))
        .and_then(|v| v.as_u64())
        .unwrap_or(0);
    let candidates_tokens = resp
        .get("usageMetadata")
        .and_then(|u| u.get("candidatesTokenCount"))
        .and_then(|v| v.as_u64())
        .unwrap_or(0);
    let cache_read_input_tokens = resp
        .get("usageMetadata")
        .and_then(|u| u.get("cachedContentTokenCount"))
        .and_then(|v| v.as_u64());
    // Gemini 2.5+ exposes the thinking-token count separately as
    // `thoughtsTokenCount`. Surface it via OpenAI's
    // `completion_tokens_details.reasoning_tokens` so usage / billing
    // pipelines see the same shape OpenAI's reasoning models use.
    let thoughts_tokens = resp
        .get("usageMetadata")
        .and_then(|u| u.get("thoughtsTokenCount"))
        .and_then(|v| v.as_u64());
    // OpenAI's contract: `completion_tokens` is the total output and
    // already includes reasoning. Gemini reports candidates + thoughts
    // separately, so fold them here — otherwise total_tokens
    // undercounts by the thinking budget.
    let completion_tokens = candidates_tokens.saturating_add(thoughts_tokens.unwrap_or(0));

    let mut usage = json!({
        "prompt_tokens": prompt_tokens,
        "completion_tokens": completion_tokens,
        "total_tokens": prompt_tokens.saturating_add(completion_tokens)
    });
    if let Some(value) = cache_read_input_tokens {
        usage["cache_read_input_tokens"] = json!(value);
    }
    if let Some(value) = thoughts_tokens {
        usage["completion_tokens_details"] = json!({ "reasoning_tokens": value });
    }

    json!({
        "id": resp.get("responseId").cloned().unwrap_or_else(|| json!(BRIDGE_FALLBACK_OPENAI_RESPONSE_ID)),
        "object": "chat.completion",
        "created": current_unix_ts(),
        "model": fallback_model,
        "choices": [{
            "index": 0,
            "message": message,
            "finish_reason": finish_reason
        }],
        "usage": usage
    })
}

fn parse_tool_content(content: Option<&Value>) -> Value {
    match content {
        Some(Value::String(value)) => {
            serde_json::from_str(value).unwrap_or_else(|_| json!({ "content": value }))
        }
        Some(other) => other.clone(),
        None => json!({}),
    }
}

fn build_tool_response_part(
    tool_message: &Value,
    tool_names_by_call_id: &HashMap<String, String>,
) -> Value {
    let call_id = tool_message
        .get("tool_call_id")
        .and_then(|v| v.as_str())
        .unwrap_or("");
    // Gemini matches by function name, not ID — fall back to call_id
    // when the assistant message with the original tool_call is not in history.
    let name = tool_names_by_call_id
        .get(call_id)
        .filter(|n| !n.is_empty())
        .cloned()
        .unwrap_or_else(|| call_id.to_string());
    let response = parse_tool_content(tool_message.get("content"));
    json!({
        "functionResponse": {
            "id": call_id,
            "name": name,
            "response": response
        }
    })
}

fn extract_openai_text(content: Option<&Value>) -> String {
    match content {
        Some(Value::String(s)) => s.clone(),
        Some(Value::Array(parts)) => parts
            .iter()
            .filter_map(|part| part.get("text").and_then(|v| v.as_str()))
            .collect::<Vec<_>>()
            .join("\n"),
        Some(Value::Null) | None => String::new(),
        Some(other) => other.to_string(),
    }
}

/// Convert OpenAI Chat Completions content (string or array of content parts)
/// into a Vec of Gemini parts. Preserves `image_url` parts as `inlineData`
/// (for `data:` URIs) or `fileData` (for HTTP URLs). Empty content yields a
/// single empty-text part so the resulting Gemini turn is still well-formed.
fn extract_openai_parts_for_gemini(content: Option<&Value>) -> Vec<Value> {
    let mut out: Vec<Value> = Vec::new();
    match content {
        Some(Value::String(s)) => {
            out.push(json!({ "text": s }));
        }
        Some(Value::Array(parts)) => {
            for part in parts {
                if let Some(p) = openai_content_part_to_gemini_part(part) {
                    out.push(p);
                }
            }
        }
        Some(Value::Null) | None => {}
        Some(other) => {
            out.push(json!({ "text": other.to_string() }));
        }
    }
    if out.is_empty() {
        out.push(json!({ "text": "" }));
    }
    out
}

/// Convert a single OpenAI Chat content part to a Gemini part. Returns `None`
/// for unrecognised types so they are silently dropped (matches the OpenAI →
/// Anthropic policy for unknown parts).
fn openai_content_part_to_gemini_part(part: &Value) -> Option<Value> {
    let part_type = part.get("type").and_then(|v| v.as_str());
    match part_type {
        // Bare strings inside a content array — treat as text.
        None | Some("text") => part
            .get("text")
            .and_then(|v| v.as_str())
            .map(|t| json!({ "text": t })),
        Some("image_url") => openai_image_url_part_to_gemini_part(part),
        _ => part
            .get("text")
            .and_then(|v| v.as_str())
            .map(|t| json!({ "text": t })),
    }
}

/// Map an OpenAI `image_url` content part to a Gemini `inlineData` (data: URI)
/// or `fileData` (HTTP URL) part. Accepts both string and object shapes for the
/// `image_url` field.
fn openai_image_url_part_to_gemini_part(part: &Value) -> Option<Value> {
    let url = part.get("image_url").and_then(|iu| match iu {
        Value::String(s) => Some(s.as_str()),
        Value::Object(o) => o.get("url").and_then(|v| v.as_str()),
        _ => None,
    })?;

    if let Some(rest) = url.strip_prefix("data:") {
        let (meta, data) = rest.split_once(',')?;
        let mime = meta
            .split(';')
            .next()
            .filter(|m| !m.is_empty())
            .unwrap_or("image/png");
        Some(json!({
            "inlineData": {
                "mimeType": mime,
                "data": data,
            }
        }))
    } else if url.starts_with("http://") || url.starts_with("https://") {
        // Best-effort mime sniff from extension; Gemini will reject if wrong
        // but most clients pair URL parts with correct extensions.
        let mime = mime_from_url(url);
        Some(json!({
            "fileData": {
                "fileUri": url,
                "mimeType": mime,
            }
        }))
    } else {
        None
    }
}

fn mime_from_url(url: &str) -> &'static str {
    let lower = url.to_ascii_lowercase();
    let path_only = lower.split(['?', '#']).next().unwrap_or(&lower);
    if path_only.ends_with(".png") {
        "image/png"
    } else if path_only.ends_with(".jpg") || path_only.ends_with(".jpeg") {
        "image/jpeg"
    } else if path_only.ends_with(".webp") {
        "image/webp"
    } else if path_only.ends_with(".gif") {
        "image/gif"
    } else if path_only.ends_with(".heic") {
        "image/heic"
    } else if path_only.ends_with(".heif") {
        "image/heif"
    } else {
        "image/jpeg"
    }
}

pub fn convert_gemini_to_openai_chat_request(
    body: &Value,
    model: &str,
    requires_reasoning_content: bool,
    max_tokens_cap: Option<u64>,
) -> Value {
    let mut messages: Vec<Value> = Vec::new();
    let mut pending_tool_calls: HashMap<String, VecDeque<String>> = HashMap::new();
    let mut tool_call_id_counts: HashMap<String, usize> = HashMap::new();

    // Join ALL parts — clients may split the system prompt across several.
    if let Some(parts) = body
        .get("systemInstruction")
        .and_then(|si| si.get("parts"))
        .and_then(|p| p.as_array())
    {
        let system_text = parts
            .iter()
            .filter_map(|p| p.get("text").and_then(|t| t.as_str()))
            .filter(|t| !t.is_empty())
            .collect::<Vec<_>>()
            .join("\n");
        if !system_text.is_empty() {
            messages.push(serde_json::json!({"role": "system", "content": system_text}));
        }
    }

    if let Some(contents) = body.get("contents").and_then(|c| c.as_array()) {
        for content in contents {
            let role = content
                .get("role")
                .and_then(|r| r.as_str())
                .unwrap_or("user");
            let openai_role = if role == "model" { "assistant" } else { role };
            let parts = content
                .get("parts")
                .and_then(|p| p.as_array())
                .map(|v| v.as_slice())
                .unwrap_or(&[]);

            convert_parts_to_messages(
                parts,
                openai_role,
                &mut messages,
                requires_reasoning_content,
                &mut pending_tool_calls,
                &mut tool_call_id_counts,
            );
        }
    }

    let tools: Vec<Value> = body
        .get("tools")
        .and_then(|t| t.as_array())
        .map(|tool_groups| {
            tool_groups
                .iter()
                .filter_map(|tg| tg.get("functionDeclarations"))
                .filter_map(|fd| fd.as_array())
                .flatten()
                .map(|func_decl| {
                    serde_json::json!({
                        "type": "function",
                        "function": {
                            "name": func_decl.get("name").cloned().unwrap_or_default(),
                            // Empty string, not null — strict providers reject a null description.
                            "description": func_decl.get("description").cloned().unwrap_or_else(|| serde_json::json!("")),
                            "parameters": sanitize_schema_for_gemini(func_decl.get("parameters").unwrap_or(&serde_json::json!({}))),
                        }
                    })
                })
                .collect()
        })
        .unwrap_or_default();

    let mut req = serde_json::json!({
        "model": model,
        "messages": messages,
        // Always non-streaming upstream; the router wraps the full response as one Gemini SSE event for stream paths.
        "stream": false,
    });

    if !tools.is_empty() {
        req["tools"] = Value::Array(tools);
    }

    if let Some(gc) = body.get("generationConfig") {
        // Sampling params dropped for models that reject them (o-series etc.).
        let rejects_sampling = crate::services::model_metadata::rejects_temperature(model);
        if let Some(t) = gc.get("temperature")
            && !rejects_sampling
        {
            req["temperature"] = t.clone();
        }
        if let Some(mt) = gc.get("maxOutputTokens") {
            let val = if let Some(cap) = max_tokens_cap {
                http_utils::parse_token_u64(mt)
                    .map(|n| serde_json::json!(n.min(cap)))
                    .unwrap_or(mt.clone())
            } else {
                mt.clone()
            };
            req["max_tokens"] = val;
        }
        if let Some(tp) = gc.get("topP")
            && !rejects_sampling
        {
            req["top_p"] = tp.clone();
        }
    }

    req
}

fn convert_parts_to_messages(
    parts: &[Value],
    openai_role: &str,
    messages: &mut Vec<Value>,
    requires_reasoning_content: bool,
    pending_tool_calls: &mut HashMap<String, VecDeque<String>>,
    tool_call_id_counts: &mut HashMap<String, usize>,
) {
    let mut text_parts: Vec<&str> = Vec::new();
    let mut thought_parts: Vec<&str> = Vec::new();
    let mut tool_calls: Vec<Value> = Vec::new();
    let mut tool_results: Vec<Value> = Vec::new();
    // inlineData/fileData → image_url parts; without this an attached image (or image-only turn) silently vanishes.
    let mut image_parts: Vec<Value> = Vec::new();

    for part in parts {
        if let Some(text) = part.get("text").and_then(|t| t.as_str()) {
            if text.is_empty() {
                continue;
            }
            // `thought: true` = thinking trace → reasoning_content so deepseek-thinking upstreams accept the turn.
            if part
                .get("thought")
                .and_then(|t| t.as_bool())
                .unwrap_or(false)
            {
                thought_parts.push(text);
            } else {
                text_parts.push(text);
            }
        } else if let Some(fc) = part.get("functionCall") {
            let name = fc.get("name").and_then(|n| n.as_str()).unwrap_or("");
            let call_id = extract_part_call_id(fc)
                .map(|id| uniquify_tool_call_id(id.to_string(), tool_call_id_counts))
                .unwrap_or_else(|| synthesize_tool_call_id(name, tool_call_id_counts));
            queue_pending_tool_call_id(pending_tool_calls, name, call_id.clone());
            let args = fc
                .get("args")
                .map(|a| serde_json::to_string(a).unwrap_or_default())
                .unwrap_or_default();
            tool_calls.push(serde_json::json!({
                "id": call_id,
                "type": "function",
                "function": {"name": name, "arguments": args}
            }));
        } else if let Some(fr) = part.get("functionResponse") {
            let name = fr.get("name").and_then(|n| n.as_str()).unwrap_or("");
            let call_id = extract_part_call_id(fr)
                .and_then(|explicit_id| {
                    take_pending_tool_call_id(pending_tool_calls, name, explicit_id)
                        .or_else(|| pop_pending_tool_call_id(pending_tool_calls, name))
                        .or_else(|| Some(explicit_id.to_string()))
                })
                .or_else(|| pop_pending_tool_call_id(pending_tool_calls, name))
                .unwrap_or_else(|| synthesize_tool_call_id(name, tool_call_id_counts));
            let response = fr
                .get("response")
                .map(|r| serde_json::to_string(r).unwrap_or_default())
                .unwrap_or_default();
            tool_results.push(serde_json::json!({
                "role": "tool",
                "tool_call_id": call_id,
                "content": response
            }));
        } else if let Some(inline) = part.get("inlineData").or_else(|| part.get("inline_data")) {
            let mime = inline
                .get("mimeType")
                .or_else(|| inline.get("mime_type"))
                .and_then(|m| m.as_str())
                .unwrap_or("image/png");
            if let Some(data) = inline.get("data").and_then(|d| d.as_str()) {
                image_parts.push(serde_json::json!({
                    "type": "image_url",
                    "image_url": {"url": format!("data:{mime};base64,{data}")}
                }));
            }
        } else if let Some(file) = part.get("fileData").or_else(|| part.get("file_data"))
            && let Some(uri) = file
                .get("fileUri")
                .or_else(|| file.get("file_uri"))
                .and_then(|u| u.as_str())
        {
            image_parts.push(serde_json::json!({
                "type": "image_url",
                "image_url": {"url": uri}
            }));
        }
    }

    if !tool_results.is_empty() {
        for tr in tool_results {
            messages.push(tr);
        }
        // Carry a tool's sibling image (plus text) as a follow-up user message — OpenAI tool messages can't hold image parts.
        if !image_parts.is_empty() {
            let mut parts: Vec<Value> = text_parts
                .iter()
                .map(|t| serde_json::json!({"type": "text", "text": t}))
                .collect();
            parts.append(&mut image_parts);
            messages.push(serde_json::json!({"role": "user", "content": parts}));
        }
    } else if !tool_calls.is_empty() {
        let content_str = text_parts.join(" ");
        let mut msg = serde_json::json!({
            "role": openai_role,
            "content": if content_str.is_empty() { Value::Null } else { Value::String(content_str) },
            "tool_calls": tool_calls,
        });
        if openai_role == "assistant" {
            attach_reasoning_content(
                &mut msg,
                &thought_parts,
                &text_parts,
                requires_reasoning_content,
            );
        }
        messages.push(msg);
    } else if !text_parts.is_empty() || !image_parts.is_empty() {
        // Skip empty-text turns — strict providers / Responses API gateways reject empty content strings.
        let content = if image_parts.is_empty() {
            Value::String(text_parts.join("\n"))
        } else {
            let mut parts: Vec<Value> = text_parts
                .iter()
                .map(|t| serde_json::json!({"type": "text", "text": t}))
                .collect();
            parts.extend(image_parts);
            Value::Array(parts)
        };
        let mut msg = serde_json::json!({"role": openai_role, "content": content});
        if openai_role == "assistant" {
            attach_reasoning_content(
                &mut msg,
                &thought_parts,
                &text_parts,
                requires_reasoning_content,
            );
        }
        messages.push(msg);
    } else if !thought_parts.is_empty() && openai_role == "assistant" {
        // Thought-only turn: preserve reasoning so deepseek doesn't see an empty assistant turn.
        let mut msg = serde_json::json!({"role": openai_role, "content": Value::Null});
        attach_reasoning_content(
            &mut msg,
            &thought_parts,
            &text_parts,
            requires_reasoning_content,
        );
        messages.push(msg);
    }
}

/// Real `thought: true` parts win (round-trip verbatim); fall back to placeholder/text echo only when absent and the upstream requires the field.
fn attach_reasoning_content(
    msg: &mut Value,
    thought_parts: &[&str],
    text_parts: &[&str],
    requires_reasoning_content: bool,
) {
    if !thought_parts.is_empty() {
        msg["reasoning_content"] = Value::String(thought_parts.join("\n"));
        return;
    }
    if !requires_reasoning_content {
        return;
    }
    let fallback = if text_parts.is_empty() {
        " ".to_string()
    } else {
        text_parts.join("\n")
    };
    msg["reasoning_content"] = Value::String(fallback);
}

pub(crate) fn extract_part_call_id(part: &Value) -> Option<&str> {
    for key in ["id", "call_id", "callId", "tool_call_id"] {
        if let Some(id) = part.get(key).and_then(|v| v.as_str())
            && !id.is_empty()
        {
            return Some(id);
        }
    }
    None
}

pub(crate) fn synthesize_tool_call_id(
    tool_name: &str,
    tool_call_id_counts: &mut HashMap<String, usize>,
) -> String {
    let normalized_name: String = tool_name
        .chars()
        .map(|c| {
            if c.is_ascii_alphanumeric() || c == '_' || c == '-' {
                c
            } else {
                '_'
            }
        })
        .collect();
    let safe_name = if normalized_name.is_empty() {
        "tool"
    } else {
        &normalized_name
    };
    uniquify_tool_call_id(format!("call_{}", safe_name), tool_call_id_counts)
}

pub(crate) fn uniquify_tool_call_id(
    base_id: String,
    tool_call_id_counts: &mut HashMap<String, usize>,
) -> String {
    let count = tool_call_id_counts.entry(base_id.clone()).or_insert(0);
    *count += 1;
    if *count == 1 {
        base_id
    } else {
        format!("{}_{}", base_id, *count)
    }
}

pub(crate) fn queue_pending_tool_call_id(
    pending_tool_calls: &mut HashMap<String, VecDeque<String>>,
    tool_name: &str,
    call_id: String,
) {
    pending_tool_calls
        .entry(tool_name.to_string())
        .or_default()
        .push_back(call_id);
}

pub(crate) fn pop_pending_tool_call_id(
    pending_tool_calls: &mut HashMap<String, VecDeque<String>>,
    tool_name: &str,
) -> Option<String> {
    let result = pending_tool_calls
        .get_mut(tool_name)
        .and_then(|queue| queue.pop_front());
    if pending_tool_calls
        .get(tool_name)
        .is_some_and(|queue| queue.is_empty())
    {
        pending_tool_calls.remove(tool_name);
    }
    result
}

pub(crate) fn take_pending_tool_call_id(
    pending_tool_calls: &mut HashMap<String, VecDeque<String>>,
    tool_name: &str,
    explicit_id: &str,
) -> Option<String> {
    let result = pending_tool_calls.get_mut(tool_name).and_then(|queue| {
        queue
            .iter()
            .position(|id| id == explicit_id)
            .and_then(|index| queue.remove(index))
    });
    if pending_tool_calls
        .get(tool_name)
        .is_some_and(|queue| queue.is_empty())
    {
        pending_tool_calls.remove(tool_name);
    }
    result
}

pub fn convert_openai_chat_to_gemini_response(body: &Value) -> Value {
    let empty_msg = serde_json::json!({"role": "assistant", "content": ""});
    let choices = body.get("choices").and_then(|c| c.as_array());
    let choice = choices
        .and_then(|arr| arr.first())
        .cloned()
        .unwrap_or(serde_json::json!({}));
    let message = choice.get("message").cloned().unwrap_or(empty_msg);
    let finish_reason = choice
        .get("finish_reason")
        .and_then(|r| r.as_str())
        .unwrap_or("stop");

    let gemini_finish = match finish_reason {
        "stop" | "tool_calls" => "STOP",
        "length" => "MAX_TOKENS",
        "content_filter" => "SAFETY",
        _ => "OTHER",
    };

    let parts = message_to_gemini_parts(&message);

    let candidate = serde_json::json!({
        "content": {"parts": parts, "role": "model"},
        "finishReason": gemini_finish,
        "index": 0,
    });

    let mut result = serde_json::json!({"candidates": [candidate]});

    if let Some(usage) = body.get("usage") {
        let mut usage_metadata = serde_json::json!({
            "promptTokenCount": usage.get("prompt_tokens").cloned().unwrap_or(Value::Null),
            "candidatesTokenCount": usage.get("completion_tokens").cloned().unwrap_or(Value::Null),
            "totalTokenCount": usage.get("total_tokens").cloned().unwrap_or(Value::Null),
        });
        if let Some(value) = usage.get("cache_read_input_tokens").cloned().or_else(|| {
            usage
                .get("prompt_tokens_details")
                .and_then(|details| details.get("cached_tokens"))
                .cloned()
        }) {
            usage_metadata["cachedContentTokenCount"] = value;
        }
        result["usageMetadata"] = usage_metadata;
    }

    result
}

pub fn convert_openai_chat_to_gemini_sse(body: &Value) -> String {
    let gemini_response = convert_openai_chat_to_gemini_response(body);
    let json = serde_json::to_string(&gemini_response).unwrap_or_default();
    format!("data: {}\n\n", json)
}

fn message_to_gemini_parts(message: &Value) -> Vec<Value> {
    let mut parts = Vec::new();

    // reasoning_content → leading `thought: true` part; without it deepseek-reasoner rejects the next turn ("must be passed back to the API").
    if let Some(reasoning) = message.get("reasoning_content").and_then(|c| c.as_str())
        && !reasoning.is_empty()
    {
        parts.push(serde_json::json!({"text": reasoning, "thought": true}));
    }

    if let Some(text) = message.get("content").and_then(|c| c.as_str())
        && !text.is_empty()
    {
        parts.push(serde_json::json!({"text": text}));
    }

    if let Some(tool_calls) = message.get("tool_calls").and_then(|t| t.as_array()) {
        for tc in tool_calls {
            let name = tc["function"]["name"].as_str().unwrap_or("");
            // Some providers return arguments as a JSON string; others as an object
            let args: Value = match &tc["function"]["arguments"] {
                Value::String(s) => serde_json::from_str(s).unwrap_or(serde_json::json!({})),
                obj @ Value::Object(_) => obj.clone(),
                _ => serde_json::json!({}),
            };
            // Strip null-valued args — some models emit explicit nulls that crash consumers (e.g. Claude Code's diff renderer).
            let args = strip_null_args(&args);
            parts.push(serde_json::json!({"functionCall": {"name": name, "args": args}}));
        }
    }

    if parts.is_empty() {
        let text = message
            .get("content")
            .and_then(|c| c.as_str())
            .unwrap_or("");
        parts.push(serde_json::json!({"text": text}));
    }

    parts
}

/// Strip top-level null-valued keys — some models emit nulls that crash consumers (e.g. Claude Code's diff renderer).
fn strip_null_args(args: &Value) -> Value {
    if let Some(obj) = args.as_object() {
        let mut cleaned = serde_json::Map::new();
        for (k, v) in obj {
            if !v.is_null() {
                cleaned.insert(k.clone(), v.clone());
            }
        }
        Value::Object(cleaned)
    } else {
        args.clone()
    }
}

